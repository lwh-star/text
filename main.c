#include<stdio.h>
int main() {
	int maxz = 0, minz = 0, maxf = 0, minf = 0, zs = 0, fs = 0, a = 0;

	for (int i = 0; i < 20; i++) {	//循环20次输入
		scanf("%d", &a);	//输入整数，并记录返回值
		if (a > 0) {

			if (zs == 0) {
				maxz = minz = a;
				zs++;
				continue;
			}
			if (maxz <= a) {
				maxz = a;
				continue;
			}
			if (minz >= a) {
				minz = a;
				continue;
			}
			else
				continue;
		}

		else if (a < 0) {
			if (fs == 0) {
				maxf = minf = a;
				fs++;
				continue;
			}
			if (maxf <= a) {
				maxf = a;
				continue;
			}
			if (minf >= a) {
				minf = a;
				continue;
			}
			else
				continue;
		}

		else if (a == 0) {
			continue;

	}

}
	printf("最大正整数为： %d\n最小正整数为： %d\n最大负整数为： %d\n最小负整数为： %d\n", maxz, minz, maxf, minf);
}
